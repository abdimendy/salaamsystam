function resolve(lang, key) {
  const order = lang === 'so' ? ['so', 'en'] : ['en', 'so'];
  const parts = key.split('.');
  for (const locale of order) {
    let node = translations[locale];
    let ok = true;
    for (const p of parts) {
      node = node?.[p];
      if (node === undefined) {
        ok = false;
        break;
      }
    }
    if (ok && typeof node === 'string') return node;
  }
  return null;
}

/** @param {'en'|'so'} lang @param {string} key @param {Record<string, string|number>} [vars] */
export function t(lang, key, vars = {}) {
  let str = resolve(lang, key);
  if (str == null) return key;
  Object.entries(vars).forEach(([k, v]) => {
    str = str.replaceAll(`{${k}}`, String(v));
  });
  return str;
}
